//String Type Case Methods Example
    
var stringValue = "hello world";
print(stringValue.toLocaleUpperCase());  //"HELLO WORLD"
print(stringValue.toUpperCase());        //"HELLO WORLD"
print(stringValue.toLocaleLowerCase());  //"hello world"
print(stringValue.toLowerCase());        //"hello world"

    