//Math Object Rounding Example
    
print(Math.ceil(25.9));     //26
print(Math.ceil(25.5));     //26
print(Math.ceil(25.1));     //26

print(Math.round(25.9));    //26
print(Math.round(25.5));    //26
print(Math.round(25.1));    //25
        
print(Math.floor(25.9));    //25
print(Math.floor(25.5));    //25
print(Math.floor(25.1));    //25

    