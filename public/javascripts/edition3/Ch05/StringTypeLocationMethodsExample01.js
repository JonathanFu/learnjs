//String Type Location Methods Example
    
var stringValue = "hello world";
print(stringValue.indexOf("o"));         //4
print(stringValue.lastIndexOf("o"));     //7
print(stringValue.indexOf("o", 6));         //7
print(stringValue.lastIndexOf("o", 6));     //4                

    