//Determining Type Example 1
var s = "Nicholas";
var b = true;
var i = 22;
var u;
var n = null;
var o = new Object();

print(typeof s);   //string
print(typeof i);   //number
print(typeof b);   //Boolean
print(typeof u);   //undefined
print(typeof n);   //object
print(typeof o);   //object

    