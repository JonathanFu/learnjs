//Number Example 5
  
var num1 = parseInt("1234blue");    //1234
var num2 = parseInt("");            //NaN
var num3 = parseInt("0xA");         //10 - hexadecimal
var num4 = parseInt(22.5);          //22
var num5 = parseInt("70");          //70 - decimal
var num6 = parseInt("0xf");         //15 – hexadecimal

print(num1);
print(num2);
print(num3);
print(num4);
print(num5);
print(num6);



    