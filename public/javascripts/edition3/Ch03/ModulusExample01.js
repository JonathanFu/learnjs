//Modulus Example 1
    
print(26 % 5);          //1
print(Infinity % 3);    //NaN
print(3 % 0);           //NaN
print(5 % Infinity);    //5
print(0 % 10);          //0
print(true % 25);       //1
print(3 % false);       //NaN
       
    