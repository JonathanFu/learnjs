//Divide Example 1
    
print(5 / 5);           //1
print(5 / NaN);         //NaN
print(Infinity / Infinity);    //NaN
print(Infinity / 2);    //Infinity
print(5 / 0);           //Infinity
print(10 / true);       //10
print(10 / false);      //Infinity
       
    