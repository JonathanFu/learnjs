//Array Type Slice Example
    
var colors = ["red", "green", "blue", "yellow", "purple"];
var colors2 = colors.slice(1);
var colors3 = colors.slice(1,4);

print(colors2);   //green,blue,yellow,purple
print(colors3);   //green,blue,yellow

    