//Number Example 8
  
var num1 = parseInt("010");         //8 – parsed as octal
var num2 = parseInt("010", 8);      //8 – parsed as octal
var num3 = parseInt("010", 10);     //10 – parsed as decimal

print(num1);
print(num2);
print(num3);


    