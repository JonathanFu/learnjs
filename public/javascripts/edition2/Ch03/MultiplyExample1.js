//Multiply Example 1
    
print(5 * 6);           //30
print(5 * NaN);         //NaN
print(Infinity * 0);    //NaN
print(Infinity * 2);    //Infinity
print("5" * 5);         //25
print(true * 10);       //10
print(false * 10);      //0
       
    